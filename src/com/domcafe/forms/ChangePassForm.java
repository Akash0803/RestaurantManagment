    private void chkShow1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_chkShow1ItemStateChanged
        // show password
        if(chkShow1.isSelected()){ //Show Password
            txtnew.setEchoChar('\u0000');
            txtold.setEchoChar('\u0000');
            txtnew2.setEchoChar('\u0000');
        }else{ //Hide Password char
            txtold.setEchoChar('\u25cf');
            txtnew.setEchoChar('\u25cf');
            txtnew2.setEchoChar('\u25cf');
        }
    }//GEN-LAST:event_chkShow1ItemStateChanged

