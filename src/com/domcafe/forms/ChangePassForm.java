    private void btnchangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchangeActionPerformed
                 //change pass 
                 String oldpass=txtold.getText();
                 String newpass=txtnew.getText();
                 String cnewpass=txtnew2.getText();
                 if(!oldpass.equals(AppStatus.password)){
                     Utility.showWarn("Please enter valid old password..","Wrong Password");
                     txtold.requestFocus();
                     return;
                 }
                 
                 if(!newpass.equals(cnewpass)){
            Utility.showWarn("New password and confirm new password mismatch..", "Password Mismatch");
            txtnew.requestFocus();
            return;
        }
        db.execute("Update Login set Password=? where UserName=?", newpass,AppStatus.username);
        Utility.showMsg("Password changed successfully...", "Change Success");
        dispose();
        
             
        
    }//GEN-LAST:event_btnchangeActionPerformed

