    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // delete
         try{
        int i=table1.getSelectedRow();
        if(i!=-1){            
            if(Utility.showConfirm("Delete selected Invoice?", "Confirm Delete")==0){
                String no=table1.getValueAt(i, 0)+"";
                db.execute("delete from Invoice where BillNo=?", no);
                db.fillTable(table1, "select * from Invoice");
            }
        }else{
            Utility.showWarn("Please select record for deletion....", "Select Record");
        }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btndeleteActionPerformed

    private void bnteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnteditActionPerformed
          try{
               File f=new File("src/Reports/Billmas.jrxml");
              
        int i=table1.getSelectedRow();
        if(i!=-1){
            String roll=table1.getValueAt(i, 0)+"";
            HashMap<String,Object> map=new HashMap<>(); //collection of key/value pairs
            map.put("p1", roll);
            da.displayReport(f.getAbsolutePath(), map);
            
       
        }else{
            Utility.showWarn("Please select raw to print ","Select");
        }
         }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_bnteditActionPerformed

