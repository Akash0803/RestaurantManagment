    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
             try {
                 //login
                 if(txtuser.getText().equals(""))
        {
            lblerror.setText("Username is Required");
            txtuser.requestFocus();
            return;
        }
                 if(txtpass.getText().equals(""))
        {
            lblerror.setText("Password is Required");
            txtpass.requestFocus();
            return;
        }
                 String user=txtuser.getText();
                 String pass=txtpass.getText();
                 ResultSet rs=db.getData("select * from Login where UserName=? and Password=?", user,pass);
                 if(rs.next()){
                     //Valid
                     if(user.equals("admin")){
                         Utility.showWarn("you can't remove admin..!","Warning");
                         return;
                     }
                     if(Utility.showConfirm("Do you want to remove User:- "+user+ " ?", "Confirm Delete")==0){
                       db.execute("delete from login where UserName=? and Password=?",user,pass);
                       Utility.showMsg("User "+user+" Succesfully Removed..","Deleted");
                       dispose();
                     }else{
                        txtuser.setText("");
                        txtpass.setText("");
                        txtuser.requestFocus();
                     }
                     
                 }else{
                     Utility.showWarn("Invalid UserName or Password... try again", "Error");
                     txtuser.setText("");
                     txtpass.setText("");
                     txtuser.requestFocus();
                     
                 }} catch (SQLException ex) {
                 Logger.getLogger(UserDeleteForm.class.getName()).log(Level.SEVERE, null, ex);
             }
        
    }//GEN-LAST:event_btnloginActionPerformed

