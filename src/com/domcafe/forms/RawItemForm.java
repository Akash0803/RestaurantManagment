    private void bntedit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntedit1ActionPerformed
        try{
        int i=table1.getSelectedRow();
        if(i!=-1){
            Object no=table1.getValueAt(i, 0);
            ResultSet rs=db.getData("select * from Material where MrNo=?", no);
            if(rs.next()){
//                Object name=rs.getObject(2);
//                Object price=rs.getObject(5);
//                Object qty=rs.getObject(3);
//                Object unit=rs.getObject(4);                
                       
                txtno.setText(rs.getString(1));
                cmbname.setSelectedItem(rs.getObject(2));
                txtunit.setText(rs.getString(4));
                txtqty.setText(rs.getString(3));
                txtprice.setText(rs.getString(5));                
                btnsave.setText("Update");                
                txtqty.requestFocus();
            
            }else{
                Utility.showWarn("Record not found..", "No Record");
            }
            
        }else{
            Utility.showWarn("Please select row to edit","Select");
        }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_bntedit1ActionPerformed

