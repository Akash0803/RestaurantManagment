    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
   if(txtunit.getText().equals(""))
        {
            lblerror.setText("Unit is Required");
            txtunit.requestFocus();
            return;
        }
        String no=txtno.getText();        
        
        String name=cmbname.getSelectedItem()+"";
        
        String unit=txtunit.getText();
        String qty=txtqty.getText();
        String price=txtprice.getText();
        String btntext=btnsave.getText(); //Save
        
        if(btntext.equals("Save")){
            db.execute("Insert into Material (MaterialName,Qty,Unit,Price) values(?,?,?,?)",name,qty,unit,price);
            Utility.showMsg("Item entry is saved..","Saved");
        }else{
            db.execute("Update Material set MaterialName=?,Qty=?,Unit=?,Price=? where MNo=?",name,qty,unit,price,no);
            JOptionPane.showMessageDialog(this,"Item entry is updated","Upated", JOptionPane.QUESTION_MESSAGE);
        }
        db.fillTable2(table1, "select * from Material");
     

             
                 
        
    }//GEN-LAST:event_btnsaveActionPerformed

