    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
         if(txtprice.getText().equals(""))
        {
            lblerror.setText("Price is Required");
            txtprice.requestFocus();
            return;
        }
         if(file.equals(null)){
              lblerror.setText("Must Upload Pic Before Save");
          
            return;
         }
        String no=txtno.getText();
        String name=txtname.getText();
        String category=cmbcategory.getSelectedItem()+"";
        String unit=txtunit.getText();
        String price=txtprice.getText();
        String btntext=btnsave.getText(); //Save

        if(btntext.equals("Save")){
            db.execute("Insert into Menu (Name,Category,Unit,Price,Image) values(?,?,?,?,?)", name,category,unit,price,file);
            Utility.showMsg("Menu entry is saved..","Saved");
        }else{
            db.execute("Update Menu set Name=?,Category=?,Unit=?,Price=?,Image=? where MenuNo=?", name,category,unit,price,file,no);
            JOptionPane.showMessageDialog(this,"Menu entry is updated","Upated", JOptionPane.QUESTION_MESSAGE);
        }
        db.fillTable2(table1, "select * from Menu");

    }//GEN-LAST:event_btnsaveActionPerformed

