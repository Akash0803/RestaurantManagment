    private void bntedit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntedit1ActionPerformed
        try{
        int i=table1.getSelectedRow();
        if(i!=-1){
            Object no=table1.getValueAt(i, 0);
            ResultSet rs=db.getData("select * from Menu where MenuNo=?", no);
            if(rs.next()){
                Object name=rs.getObject(2);                
                Object category=rs.getObject(3);
                Object price=rs.getObject(5);
                Object unit=rs.getObject(4);                
                InputStream in=rs.getBinaryStream(6);                
                byte b[]=new byte[in.available()];
                in.read(b);
                file=File.createTempFile("city", null);                
                FileOutputStream fos=new FileOutputStream(file);
                fos.write(b);
                fos.close();
                Utility.setLabelImage(lblimage, file);                
                txtno.setText(no+"");
                txtname.setText(name+"");
                cmbcategory.setSelectedItem(category);
                txtprice.setText(price+"");
                txtunit.setText(unit+"");
                btnsave.setText("Update");                
                txtname.requestFocus();
            
            }else{
                Utility.showWarn("Menu not found..", "No Menu");
            }
            
        }else{
            Utility.showWarn("Please select row to edit","Select");
        }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_bntedit1ActionPerformed

