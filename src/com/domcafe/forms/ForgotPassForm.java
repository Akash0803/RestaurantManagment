    private void txtuserFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtuserFocusLost
        // Forgotpass
         try{
        String user=txtuser.getText();
        ResultSet rs=db.getData("select SecQ,SecA,Password from Login where UserName=?", user);
        if(rs.next()){
            txtsecq.setText(rs.getString(1));
            ans=rs.getString(2);
            pass=rs.getString(3);
            flag=true;            
        }else{
            Utility.showWarn("Please enter valid username", "Invalid UserName");
            flag=false;
        }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_txtuserFocusLost

