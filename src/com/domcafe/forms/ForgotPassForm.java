    private void btnchangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchangeActionPerformed
                 //recover
                                   if(txtseca.getText().equals(""))
        {
            lblerror.setText("Aanswer is Required");
            txtseca.requestFocus();
            return;
        }
                                  
                  if(flag){
        String myans=txtseca.getText();
        if(myans.equals(ans)){
            Utility.showMsg("Your Password "+pass, "Password Recovered");
        }else{
            Utility.showWarn("Wrong Answer.... try again", "Wrong Answer");
        }
        }else{
            Utility.showWarn("Invalid Username... please enter valid username.... try again", "Wrong UserName");
        }
                
        
             
        
    }//GEN-LAST:event_btnchangeActionPerformed

